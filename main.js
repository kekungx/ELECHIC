document.addEventListener('DOMContentLoaded', () => {
    const articleGrid = document.querySelector('.article-grid');
    const languageSelect = document.getElementById('language-select');
    const themeToggle = document.getElementById('theme-toggle');
    const body = document.body;

    // Load saved preferences
    let currentLang = localStorage.getItem('elechic_lang') || 'ko';
    let currentTheme = localStorage.getItem('elechic_theme') || 'dark';

    languageSelect.value = currentLang;
    if (currentTheme === 'light') {
        body.classList.add('light-mode');
        themeToggle.textContent = '‚òÄÔ∏è';
    }

    const updateContent = (lang) => {
        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            if (translations[lang][key]) {
                el.textContent = translations[lang][key];
            }
        });
        renderArticles(lang);
    };

    const renderArticles = (lang) => {
        articleGrid.innerHTML = '';
        const articles = [
            {
                key: 'article1',
                image: 'https://images.unsplash.com/photo-1617966225287-7a5c432b7a8a?q=80&w=2070&auto=format&fit=crop'
            },
            {
                key: 'article2',
                image: 'https://images.unsplash.com/photo-1593113598332-cd288d649433?q=80&w=2070&auto=format&fit=crop'
            },
            {
                key: 'article3',
                image: 'https://images.unsplash.com/photo-1585324388597-3a669c364b38?q=80&w=1932&auto=format&fit=crop'
            }
        ];

        articles.forEach(article => {
            const articleCard = document.createElement('div');
            articleCard.classList.add('article-card');
            articleCard.innerHTML = `
                <img src="${article.image}" alt="${translations[lang][article.key + '.title']}">
                <div class="article-content">
                    <h3>${translations[lang][article.key + '.title']}</h3>
                    <p>${translations[lang][article.key + '.excerpt']}</p>
                </div>
            `;
            articleGrid.appendChild(articleCard);
        });
    };

    // Event Listeners
    languageSelect.addEventListener('change', (e) => {
        currentLang = e.target.value;
        localStorage.setItem('elechic_lang', currentLang);
        updateContent(currentLang);
    });

    themeToggle.addEventListener('click', () => {
        body.classList.toggle('light-mode');
        const isLight = body.classList.contains('light-mode');
        themeToggle.textContent = isLight ? '‚òÄÔ∏è' : 'üåô';
        localStorage.setItem('elechic_theme', isLight ? 'light' : 'dark');
    });

    // Initial render
    updateContent(currentLang);
});
